namespace Mud.Domain;

public enum Sizes
{
    Tiny   = 1, // small birds and anything smaller
    Small  = 2, // large birds (ducks and up) to halflings
    Medium = 3, // elves and dwarves to humans
    Large  = 4, // ogres, gnolls, and other large humanoids
    Huge   = 5, // giants, small dragons, and wyverns  
    Giant  = 6 // for VERY large critters, i.e. dragons, titans, and the biggest giants
}
